          }
        }
      } catch (error) {
        console.error('Î≤àÏó≠ ÏÉÅÌÉú ÌôïÏù∏ Ï§ë Ïò§Î•ò:', error);
        this.isTranslated = false;
      }
    },

    // FirebaseÏóê Î≤àÏó≠ Í≤∞Í≥º Ï†ÄÏû•
    async saveTranslationToFirebase(translatedContent) {
      try {
        const auth = getAuth();
        const user = auth.currentUser;
        
        if (!user || !this.selectedPlace) return;

        const response = await fetch('/api/gemini/save-translation', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({
            uid: user.uid,
            content_id: this.selectedPlace.contentid,
            translated_content: translatedContent,
            target_lang: this.userLanguage,
            region: this.region,
            category: this.category
          }),
        });

        if (response.ok) {
          this.isTranslated = true;
          console.log('Î≤àÏó≠ Í≤∞Í≥ºÍ∞Ä FirebaseÏóê Ï†ÄÏû•ÎêòÏóàÏäµÎãàÎã§.');
