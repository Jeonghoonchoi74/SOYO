          this.isTranslated = result.isTranslated;
          
          // Ïù¥ÎØ∏ Î≤àÏó≠Îêú ÎÇ¥Ïö©Ïù¥ ÏûàÎã§Î©¥ Î°úÎìú
          if (result.isTranslated && result.translated_content) {
